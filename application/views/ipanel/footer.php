        ],

    });

   
    

    $('#btn-filter').click(function(){ //button filter event click
        table.ajax.reload();  //just reload table
    });
    $('#btn-reset').click(function(){ //button reset event click
        $('#form-filter')[0].reset();
        table.ajax.reload();  //just reload table
    });
     
  

    $("#ordertable").on('click', '.changestatus', function(e){
        e.preventDefault();
      var order_id=$(this).attr('data-orderid');
      var status=$(this).attr('data-status');

      if(status == 'cancel')
      {
        $('#order_id').val(order_id);
       $('#cancelreason').modal('show');
       //$('#cancelreason').modal('hide');

      }else if(status == 'dispatched')
      {

       $('#dispatch_order_id').val(order_id);
       $('#dispatch').modal('show');
       $('#docno').focus();

       }
       else{ 

       $.ajax({

        url: '<?=base_url();?>changeorderstatus',
        type: 'post',
        dataType: 'json',
        data: {
                order_id:order_id,
                status:status,
              },//send the selected area value
        success: function(data)
                {
                   
                  table.ajax.reload();
                }
    });

     }
      
    });

     $("#cancelreason").on('click', '#cancelorder', function(e){
        e.preventDefault();
         $(".error").remove();
      var order_id=$('#order_id').val();
      var reason=$('#reason').val();

      if(!reason)
      {
        $('#reason').after('<span class="error" style="color:red;">Please Enter Reason</span>');
       //$('#cancelreason').modal('hide');

      }else{

       $.ajax({

        url: '<?=base_url();?>changeorderstatus',
        type: 'post',
        dataType: 'json',
        data: {
                order_id:order_id,
                status:"cancel",
                reason:reason,
              },//send the selected area value
        success: function(data)
                {
                  $('#cancelreason').modal('hide');
                  table.ajax.reload();
                }
    });

     }
      
    });

